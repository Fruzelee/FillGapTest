public class WordObject {

    String word;
    int width, height;

    public String getText() {
        return word;
    }
    public void setText(String word) {
        this.word = word;
    }

    public int getWidth() {
        return width;
    }
    public void setWidth(int width) {
        this.width = width;
    }

    public int getHeight() {
        return height;
    }
    public void setHeight(int height) {
        this.height = height;
    }
}
